#include "3-calc.h"

/**
 * op_add - Sum Operations
 * @a: First number
 * @b: Second number
 * Return: The result of operation
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Substrac Operations
 * @a: First number
 * @b: Second number
 * Return: The result of operation
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiplicate Operations
 * @a: First number
 * @b: Second number
 * Return: The result of operation
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - div Operations
 * @a: First number
 * @b: Second number
 * Return: The result of operation
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod Operations
 * @a: First number
 * @b: Second number
 * Return: The result of operation
 */

int op_mod(int a, int b)
{
	return (a % b);
}
